/******************************************************************************
 class QryFmncAdrList::VecVOrd
 ******************************************************************************/

uint QryFmncAdrList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "cty") return CTY;
	else if (s == "hkt") return HKT;
	else if (s == "hku") return HKU;
	else if (s == "aty") return ATY;
	else if (s == "cry") return CRY;

	return(0);
};

string QryFmncAdrList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == CTY) return("cty");
	else if (ix == HKT) return("hkt");
	else if (ix == HKU) return("hku");
	else if (ix == ATY) return("aty");
	else if (ix == CRY) return("cry");

	return("");
};

void QryFmncAdrList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


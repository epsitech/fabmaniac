/******************************************************************************
 class QryFmncBilList::VecVOrd
 ******************************************************************************/

uint QryFmncBilList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "dat") return DAT;
	else if (s == "prj") return PRJ;
	else if (s == "fil") return FIL;
	else if (s == "ste") return STE;

	return(0);
};

string QryFmncBilList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == DAT) return("dat");
	else if (ix == PRJ) return("prj");
	else if (ix == FIL) return("fil");
	else if (ix == STE) return("ste");

	return("");
};

void QryFmncBilList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


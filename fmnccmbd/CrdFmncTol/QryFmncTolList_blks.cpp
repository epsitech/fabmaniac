/******************************************************************************
 class QryFmncTolList::VecVOrd
 ******************************************************************************/

uint QryFmncTolList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "tit") return TIT;
	else if (s == "fab") return FAB;
	else if (s == "mnf") return MNF;

	return(0);
};

string QryFmncTolList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TIT) return("tit");
	else if (ix == FAB) return("fab");
	else if (ix == MNF) return("mnf");

	return("");
};

void QryFmncTolList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=3;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


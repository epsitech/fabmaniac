/******************************************************************************
 class QryFmncStpList::VecVOrd
 ******************************************************************************/

uint QryFmncStpList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "act") return ACT;
	else if (s == "rlt") return RLT;
	else if (s == "rlu") return RLU;
	else if (s == "stp") return STP;
	else if (s == "sta") return STA;

	return(0);
};

string QryFmncStpList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == ACT) return("act");
	else if (ix == RLT) return("rlt");
	else if (ix == RLU) return("rlu");
	else if (ix == STP) return("stp");
	else if (ix == STA) return("sta");

	return("");
};

void QryFmncStpList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


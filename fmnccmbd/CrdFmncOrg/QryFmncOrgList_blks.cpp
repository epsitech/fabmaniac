/******************************************************************************
 class QryFmncOrgList::VecVOrd
 ******************************************************************************/

uint QryFmncOrgList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "tit") return TIT;
	else if (s == "drv") return DRV;
	else if (s == "adr") return ADR;
	else if (s == "sup") return SUP;

	return(0);
};

string QryFmncOrgList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == TIT) return("tit");
	else if (ix == DRV) return("drv");
	else if (ix == ADR) return("adr");
	else if (ix == SUP) return("sup");

	return("");
};

void QryFmncOrgList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


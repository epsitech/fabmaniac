/******************************************************************************
 class QryFmncPrsList::VecVOrd
 ******************************************************************************/

uint QryFmncPrsList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "lnm") return LNM;
	else if (s == "grp") return GRP;
	else if (s == "own") return OWN;
	else if (s == "org") return ORG;
	else if (s == "adr") return ADR;

	return(0);
};

string QryFmncPrsList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == LNM) return("lnm");
	else if (ix == GRP) return("grp");
	else if (ix == OWN) return("own");
	else if (ix == ORG) return("org");
	else if (ix == ADR) return("adr");

	return("");
};

void QryFmncPrsList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


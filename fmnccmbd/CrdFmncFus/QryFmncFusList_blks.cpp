/******************************************************************************
 class QryFmncFusList::VecVOrd
 ******************************************************************************/

uint QryFmncFusList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "prs") return PRS;
	else if (s == "srf") return SRF;
	else if (s == "fab") return FAB;
	else if (s == "fpj") return FPJ;
	else if (s == "ste") return STE;

	return(0);
};

string QryFmncFusList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == PRS) return("prs");
	else if (ix == SRF) return("srf");
	else if (ix == FAB) return("fab");
	else if (ix == FPJ) return("fpj");
	else if (ix == STE) return("ste");

	return("");
};

void QryFmncFusList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


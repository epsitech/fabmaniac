/******************************************************************************
 class QryFmncSmpList::VecVOrd
 ******************************************************************************/

uint QryFmncSmpList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "sup") return SUP;
	else if (s == "art") return ART;
	else if (s == "ste") return STE;

	return(0);
};

string QryFmncSmpList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == SUP) return("sup");
	else if (ix == ART) return("art");
	else if (ix == STE) return("ste");

	return("");
};

void QryFmncSmpList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=4;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};


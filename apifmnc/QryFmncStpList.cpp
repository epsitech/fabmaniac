/******************************************************************************
 class QryFmncStpList::VecVOrd
 ******************************************************************************/

uint QryFmncStpList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("act") == 0) return ACT;
	else if (s.compare("rlt") == 0) return RLT;
	else if (s.compare("rlu") == 0) return RLU;
	else if (s.compare("stp") == 0) return STP;
	else if (s.compare("sta") == 0) return STA;

	return(0);
};

string QryFmncStpList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == ACT) return("act");
	else if (ix == RLT) return("rlt");
	else if (ix == RLU) return("rlu");
	else if (ix == STP) return("stp");
	else if (ix == STA) return("sta");

	return("");
};

/******************************************************************************
 class QryFmncStpList::StatApp
 ******************************************************************************/

QryFmncStpList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryFmncStpList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryFmncStpList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryFmncStpList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryFmncStpList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryFmncStpList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};


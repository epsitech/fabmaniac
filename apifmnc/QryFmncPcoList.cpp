/******************************************************************************
 class QryFmncPcoList::VecVOrd
 ******************************************************************************/

uint QryFmncPcoList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("srf") == 0) return SRF;
	else if (s.compare("dat") == 0) return DAT;
	else if (s.compare("pco") == 0) return PCO;
	else if (s.compare("spl") == 0) return SPL;
	else if (s.compare("fil") == 0) return FIL;
	else if (s.compare("ste") == 0) return STE;

	return(0);
};

string QryFmncPcoList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == DAT) return("dat");
	else if (ix == PCO) return("pco");
	else if (ix == SPL) return("spl");
	else if (ix == FIL) return("fil");
	else if (ix == STE) return("ste");

	return("");
};

/******************************************************************************
 class QryFmncPcoList::StatApp
 ******************************************************************************/

QryFmncPcoList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryFmncPcoList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryFmncPcoList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryFmncPcoList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryFmncPcoList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryFmncPcoList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};


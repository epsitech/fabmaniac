/******************************************************************************
 class QryFmncAdrList::VecVOrd
 ******************************************************************************/

uint QryFmncAdrList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("cty") == 0) return CTY;
	else if (s.compare("hkt") == 0) return HKT;
	else if (s.compare("hku") == 0) return HKU;
	else if (s.compare("aty") == 0) return ATY;
	else if (s.compare("cry") == 0) return CRY;

	return(0);
};

string QryFmncAdrList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == CTY) return("cty");
	else if (ix == HKT) return("hkt");
	else if (ix == HKU) return("hku");
	else if (ix == ATY) return("aty");
	else if (ix == CRY) return("cry");

	return("");
};

/******************************************************************************
 class QryFmncAdrList::StatApp
 ******************************************************************************/

QryFmncAdrList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryFmncAdrList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryFmncAdrList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryFmncAdrList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryFmncAdrList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryFmncAdrList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};

